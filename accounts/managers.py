from django.contrib import auth
from django.contrib.auth.base_user import BaseUserManager
from django.contrib.auth import load_backend
from django.conf import settings
from typing import TypeVar, Generic, TYPE_CHECKING

if TYPE_CHECKING:
    from .models import User

# Define a TypeVar for your User model
UserType = TypeVar('UserType', bound='User')

class UserManager(BaseUserManager, Generic[UserType]):
    use_in_migrations = True

    def _create_user(self, email, password, **extra_fields) -> UserType:
        """
        Create and save a user with the given email and password.
        """
        if not email:
            raise ValueError('The given email must be set')
        email = self.normalize_email(email)
        user = self.model(email=email, **extra_fields)
        user.set_password(password)
        user.save(using=self._db)
        return user

    def create_user(self, email=None, password=None, **extra_fields) -> UserType:
        extra_fields.setdefault('is_staff', False)
        extra_fields.setdefault('is_superuser', False)
        return self._create_user(email, password, **extra_fields)

    def create_superuser(self, email, password=None, **extra_fields) -> UserType:
        extra_fields.setdefault('is_staff', True)
        extra_fields.setdefault('is_superuser', True)

        if extra_fields.get('is_staff') is not True:
            raise ValueError('Superuser must have is_staff=True.')
        if extra_fields.get('is_superuser') is not True:
            raise ValueError('Superuser must have is_superuser=True.')

        return self._create_user(email, password, **extra_fields)

    def with_perm(self, perm, is_active=True, include_superusers=True, backend=None, obj=None):
        if backend is None:
            backends = settings.AUTHENTICATION_BACKENDS
            if len(backends) == 1:
                backend = backends[0]
            else:
                raise ValueError(
                    'You have multiple authentication backends configured and '
                    'therefore must provide the `backend` argument.'
                )
        elif not isinstance(backend, str):
            raise TypeError(
                'backend must be a dotted import path string (got %r).'
                % backend
            )

        backend_instance = load_backend(backend)

        if hasattr(backend_instance, 'with_perm'):
            return backend_instance.with_perm(
                perm,
                is_active=is_active,
                include_superusers=include_superusers,
                obj=obj,
            )
        return self.none()
